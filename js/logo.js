var linkHtmlLogo = document.querySelector(".HTML-logo__link");
var svgHtmlLogo = document.querySelector(".html-logo");

linkHtmlLogo.addEventListener("mouseover" ,function(){
  svgHtmlLogo.classList.add("logo-animation");
});

linkHtmlLogo.addEventListener("mouseout" ,function(){
  svgHtmlLogo.classList.remove("logo-animation");
});

